=head1 NAME

CPAN::WWW::Testers - The pre-2009 CPAN Testers website codebase

=head1 DESCRIPTION

This distribution used to generate the CPAN Testers Reports website before 
2009.

=head1 CPAN TESTERS

The CPAN Testers project was conceived back in May 1998 by Graham Barr and 
Chris Nandor as a way to provide multi-platform testing for modules uploaded
to CPAN. Today there are over 20 million test reports and more than 100 
testers regularly submitting reports each month, giving valuable feedback for
users and authors alike.

The objective of the group is to test as many of the distributions available on
CPAN as possible, on as many platforms as possible, with a variety of perl 
interpreters. The ultimate goal is to improve the portability of the 
distributions on CPAN, and provide good feedback to the authors.

This distribution used to create CPAN Testers website up until 2009. Leon 
Brocard was the original author and maintainer of this distribution, which
Barbie took over in 2008. Following the complete redesign of the website, and 
associated sites, this distribution and the tools used were no longer required.

For the current set of releases for the CPAN Testers family of websites,
please see:

  * CPAN-Testers-WWW-Reports
  * CPAN-Testers-WWW-Statistics
  * CPAN-Testers-WWW-Wiki
  * CPAN-Testers-WWW-Blog

ADVANCED WARNING - ADVANCED WARNING - ADVANCED WARNING - ADVANCED WARNING

This distribution will soon be deleted from CPAN. It is no longer applicable 
for the building of the CPAN Testers websites, and no longer maintained.

